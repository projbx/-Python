def dz1_withoutRE(l):
    res_list = []
    pack = ''
    isbeside = False
    for i in l:
        if i.islower():
            pack += i
        elif i.isupper():
            if pack:
                res_list.append(pack)
                pack = ''
            isbeside = True
        else:
            if isbeside and pack != '':
                res_list.append(pack)
            pack = ''
            isbeside=False
    return res_list

